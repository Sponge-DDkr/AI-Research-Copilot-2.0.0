# 分析指令模板 — 嵌入 System Prompt 的"数据→结论"操作守则
# Agent 在收到搜索结果后，直接按此模板分析，无需调用任何工具

ANALYSIS_TEMPLATE = """## 数据分析指南

当你收集到搜索结果或用户提供的资料后，请按以下结构直接在回复中进行分析：

### 分析要求

1. **提取关键信息**：从原始数据中提取最重要的事实、数字和观点
2. **分类整理**：将信息按主题或类别分组
3. **识别趋势**：找出数据中体现的趋势、模式或异常
4. **给出结论**：基于数据得出客观、有依据的结论

### 输出结构

分析时尽量按以下结构组织：

**📊 关键发现**
- 2-4 条最关键的信息点

**🔗 主题分类**
按主题将信息分组，每组给出小标题

**📈 趋势/模式**
如果数据量足够，描述发现的趋势

**💡 结论**
基于数据的总结（2-3 句话）

### 注意事项
- 保持客观，不要添加数据中没有的信息
- 如果数据不足以支撑某个结论，明确指出
- 用中文输出
- 分析是报告的一部分，不要写"以下是分析"之类的引语，直接输出
- 如果只需要简短分析，可以跳过四段式结构，直接输出要点"""
